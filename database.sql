--       define('DB_CHARSET', 'utf8mb4');
--     (config/db.php в git НЕ коммить, иначе сломается дома: там 127.0.0.1:3307, root, без пароля)
--  3. Выбери свою базу в phpMyAdmin -> вкладка Импорт (или SQL) -> загрузи этот файл -> Вперёд.
--
--  ГЕРОИ И ПРЕДМЕТЫ ТЕПЕРЬ ХРАНЯТСЯ В БАЗЕ (таблицы `hero` и `item`).
--  Их НЕ нужно заполнять вручную: при первом открытии сайта PHP сам создаёт эти таблицы
--  и заполняет их значениями по умолчанию из config/heroes_data.php и config/items_data.php.
--  Дальше редактировать героев и предметы можно прямо в админ-панели /admin/.
--  CREATE TABLE ниже приведены для наглядности и на случай ручного создания.
--
--  В базе также живёт пользовательский контент и текст разделов (таблица game_mechanic, делится по полю category).
-- =============================================================

SET NAMES utf8mb4;

-- -------------------------------------------------------------
-- 1. Пользователи
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS `user` (
  id_user INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  surname VARCHAR(100) NOT NULL,
  patronymic VARCHAR(100) DEFAULT NULL,
  phone VARCHAR(30) DEFAULT NULL,
  login VARCHAR(100) NOT NULL UNIQUE,
  email VARCHAR(150) NOT NULL,
  password VARCHAR(255) NOT NULL,
  registration_date DATE DEFAULT NULL,
  role VARCHAR(20) NOT NULL DEFAULT 'user'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- -------------------------------------------------------------
-- РОЛИ ПОЛЬЗОВАТЕЛЕЙ
--   'guest' — гость (в БД НЕ хранится, это просто сессия без входа)
--   'user'  — обычный пользователь (роль по умолчанию для всех новых)
--   'admin' — администратор (доступ к админ-панели /admin/)
--
-- Новые пользователи автоматически получают роль 'user'.
--
-- Если база УЖЕ создана РАНЬШЕ (без колонки role) — выполни ОДИН раз в SQL:
--    ALTER TABLE `user` ADD COLUMN role VARCHAR(20) NOT NULL DEFAULT 'user';
--
-- Чтобы назначить себя АДМИНИСТРАТОРОМ (замени ВАШ_ЛОГИН на свой логин входа):
--    UPDATE `user` SET role = 'admin' WHERE login = 'ВАШ_ЛОГИН';
-- -------------------------------------------------------------

-- -------------------------------------------------------------
-- 2. Избранное
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS favorites (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  item_type VARCHAR(30) NOT NULL,
  item_id INT NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- -------------------------------------------------------------
-- 3. Просмотры героев (для раздела «Рейтинг героев»)
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS hero_views (
  hero_id INT NOT NULL PRIMARY KEY,
  views INT NOT NULL DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- -------------------------------------------------------------
-- 4. Контент разделов (game_mechanic, делится по полю category)
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS game_mechanic (
  id_game_mechanic INT AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(255) NOT NULL,
  category VARCHAR(100) NOT NULL,
  content TEXT NOT NULL,
  created_date DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- -------------------------------------------------------------
-- 5. Обратная связь
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS feedback (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  type VARCHAR(30) NOT NULL,
  title VARCHAR(255) NOT NULL,
  content TEXT NOT NULL,
  priority VARCHAR(20) DEFAULT 'medium',
  status VARCHAR(20) DEFAULT 'new',
  admin_response TEXT DEFAULT NULL,
  created_date DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- -------------------------------------------------------------
-- 6. Герои (редактируются в админ-панели; заполняются автоматически при первом запуске)
--     image_url — ссылка на фото героя; abilities — способности, каждая с новой строки
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS hero (
  id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(150) NOT NULL,
  attr VARCHAR(20) NOT NULL,
  attr_name VARCHAR(40) NOT NULL,
  attack VARCHAR(40) DEFAULT '',
  roles VARCHAR(255) DEFAULT '',
  base_str INT DEFAULT 0,
  base_agi INT DEFAULT 0,
  base_int INT DEFAULT 0,
  gain_str VARCHAR(20) DEFAULT '0',
  gain_agi VARCHAR(20) DEFAULT '0',
  gain_int VARCHAR(20) DEFAULT '0',
  abilities TEXT,
  image_url TEXT,
  description TEXT,
  tips TEXT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- -------------------------------------------------------------
-- 7. Предметы (редактируются в админ-панели; заполняются автоматически при первом запуске)
--     category — Артефакт / Оружие / Расходники / Поддержка; image_url — ссылка на фото
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS item (
  id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(150) NOT NULL,
  category VARCHAR(40) NOT NULL,
  cost VARCHAR(40) DEFAULT '',
  components TEXT,
  bonuses TEXT,
  effects TEXT,
  strong_against TEXT,
  image_url TEXT,
  description TEXT,
  tips TEXT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

